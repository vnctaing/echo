<h2>VIE PRIVÉE</h2>

<p>Echo, le service permettant de créer et partager de contenu éphémère s’engage à respecter la vie privé de ses utilisateurs. <br>
1. À la fin de sa durée de vie, un contenu n’est pas conservé dans notre base de données et est supprimé définitivement. <br>
2. Les informations personnelles de l’utilisateur (adresse e-mail, mot de passe…) ne seront jamais diffusées ou relayées à des fins commerciales.<br> 
3. Notre équipe n’utilisera jamais le contenu des echos des utilisateurs pour un quelconque usage professionnel ou personnel.</p>


<h2>SÉCURITÉ</h2>

<p>1. En cas de diffusion de propos illicites, menaçants, haineux ou dangereux pour autrui, certaines informations sur l’auteur pourront être transmises à des autorités compétentes si celles-ci sont réclamées.<br>
